package kr.co.coduck.form;

import org.springframework.web.multipart.MultipartFile;

public class AddTestFormByExcel {
	
	private int testMainCateNo;
	private int testCateNo;
	private MultipartFile xlsfile;
	private int price;
	
	public AddTestFormByExcel() {}

	public int getTestMainCateNo() {
		return testMainCateNo;
	}

	public void setTestMainCateNo(int testMainCateNo) {
		this.testMainCateNo = testMainCateNo;
	}

	public int getTestCateNo() {
		return testCateNo;
	}

	public void setTestCateNo(int testCateNo) {
		this.testCateNo = testCateNo;
	}

	public MultipartFile getXlsfile() {
		return xlsfile;
	}

	public void setXlsfile(MultipartFile xlsfile) {
		this.xlsfile = xlsfile;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	@Override
	public String toString() {
		return "AddTestByExcel [testMainCateNo=" + testMainCateNo + ", testCateNo=" + testCateNo + ", xlsfile="
				+ xlsfile + ", price=" + price + "]";
	}
	
}
