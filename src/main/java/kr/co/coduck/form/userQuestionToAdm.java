package kr.co.coduck.form;

import java.util.ArrayList;
import java.util.List;

import org.springframework.web.multipart.MultipartFile;

public class userQuestionToAdm {

	private String title;
	private String content;
	private List<MultipartFile> filedatas = new ArrayList<MultipartFile>();
	
	public userQuestionToAdm() {}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public List<MultipartFile> getFiledatas() {
		return filedatas;
	}

	public void setFiledatas(List<MultipartFile> filedatas) {
		this.filedatas = filedatas;
	}

	@Override
	public String toString() {
		return "userQuestionToAdm [title=" + title + ", content=" + content + ", filedatas=" + filedatas + "]";
	}
}
