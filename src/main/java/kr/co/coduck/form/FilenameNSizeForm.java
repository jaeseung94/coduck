package kr.co.coduck.form;

import java.util.ArrayList;
import java.util.List;

public class FilenameNSizeForm {
	
	private List<String> filename = new ArrayList<String>();
	private List<Long> filesize = new ArrayList<Long>();
	
	public FilenameNSizeForm() {}

	public List<String> getFilename() {
		return filename;
	}

	public void setFilename(List<String> filename) {
		this.filename = filename;
	}

	public List<Long> getFilesize() {
		return filesize;
	}

	public void setFilesize(List<Long> filesize) {
		this.filesize = filesize;
	}

	@Override
	public String toString() {
		return "FilenameNSizeForm [filename=" + filename + ", filesize=" + filesize + "]";
	}
	
}
