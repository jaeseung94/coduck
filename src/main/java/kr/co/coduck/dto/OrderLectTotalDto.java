package kr.co.coduck.dto;

import java.util.List;

import kr.co.coduck.vo.Lect;
import kr.co.coduck.vo.OrdLect;

public class OrderLectTotalDto {

	private Lect lect;
	private OrdLect ordLect;
	private List<OrdLectInfoDto> ordLectInfoDtos;
	
	public OrderLectTotalDto() {}

	public Lect getLect() {
		return lect;
	}

	public void setLect(Lect lect) {
		this.lect = lect;
	}

	public OrdLect getOrdLect() {
		return ordLect;
	}

	public void setOrdLect(OrdLect ordLect) {
		this.ordLect = ordLect;
	}

	public List<OrdLectInfoDto> getOrdLectInfoDtos() {
		return ordLectInfoDtos;
	}

	public void setOrdLectInfoDtos(List<OrdLectInfoDto> ordLectInfoDtos) {
		this.ordLectInfoDtos = ordLectInfoDtos;
	}

	@Override
	public String toString() {
		return "OrderLectTotalDto [lect=" + lect + ", ordLect=" + ordLect + ", ordLectInfoDtos=" + ordLectInfoDtos
				+ "]";
	}
	
	
}
