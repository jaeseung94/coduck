package kr.co.coduck.dto;

import java.util.List;

import kr.co.coduck.vo.Category;
import kr.co.coduck.vo.Ep;
import kr.co.coduck.vo.Pagination;

public class SearchTestDto {
	
	private List<Category> upCategories;
	private List<Category> downCategories;
	private List<Ep> eps;
	private List<SearchTestDetailDto> searchTestDetailDtos;
	private Pagination pagination;
	
	public SearchTestDto() {}

	public List<Category> getUpCategories() {
		return upCategories;
	}

	public void setUpCategories(List<Category> upCategories) {
		this.upCategories = upCategories;
	}

	public List<Category> getDownCategories() {
		return downCategories;
	}

	public void setDownCategories(List<Category> downCategories) {
		this.downCategories = downCategories;
	}

	public List<Ep> getEps() {
		return eps;
	}

	public void setEps(List<Ep> eps) {
		this.eps = eps;
	}

	public List<SearchTestDetailDto> getSearchTestDetailDtos() {
		return searchTestDetailDtos;
	}

	public void setSearchTestDetailDtos(List<SearchTestDetailDto> searchTestDetailDtos) {
		this.searchTestDetailDtos = searchTestDetailDtos;
	}

	public Pagination getPagination() {
		return pagination;
	}

	public void setPagination(Pagination pagination) {
		this.pagination = pagination;
	}

	@Override
	public String toString() {
		return "SearchTestDto [upCategories=" + upCategories + ", downCategories=" + downCategories + ", eps=" + eps
				+ ", searchTestDetailDtos=" + searchTestDetailDtos + ", pagination=" + pagination + "]";
	}
	
}
